     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        
        $slug = Str::slug($request->title);
        
        $category = new Category();
       
        $category->title = $request->title;
        $category->slug = $slug;
        $category->save();
        return redirect()->route('view_category');

    }

